//Change image
function changeImage() {

    if (document.getElementById("imgClickAndChange").src = "assets/img/boxout.png") 
    {
        document.getElementById("imgClickAndChange").src = "assets/img/boxclosed.png";
    }
    else 
    {
        document.getElementById("imgClickAndChange").src = "assets/img/boxout.png";
    }
}